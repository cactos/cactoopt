package eu.cactosfp7.cactoopt.optimisationservice.config;

import java.util.Dictionary;

/**
 * Responsible for handling updates to the properties of an Optimisation Service. As receiving specific service configuration
 * is distinct from this responsibility, this interface does not subsume such services.
 * @author stier
 *
 */
public interface IOptimisationConfigurable {
    
    /**
     * Handles updated properties.
     * @param properties The updated properties.
     */
    void updated(Dictionary<String, ?> properties);
}
