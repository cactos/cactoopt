package eu.cactosfp7.cactoopt.framework.model.exceptions;

import eu.cactosfp7.cactoopt.framework.model.PhysicalMachine;
import eu.cactosfp7.cactoopt.framework.model.VirtualMachine;
import eu.cactosfp7.cactoopt.framework.model.exceptions.PlacementException;

/**
 * Thrown if a given {@link PhysicalMachine} has too little available memory
 * left to host a {@link VirtualMachine}.
 */
public class MemoryExhaustedException extends PlacementException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 736539759588994865L;

	public MemoryExhaustedException() {
		super();
	}

	public MemoryExhaustedException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MemoryExhaustedException(String message, Throwable cause) {
		super(message, cause);
	}

	public MemoryExhaustedException(String message) {
		super(message);
	}

	public MemoryExhaustedException(Throwable cause) {
		super(cause);
	}

}
