package eu.cactosfp7.cactoopt.models;


/** 
 * Initial placement of new virtual machine
 * @author jakub
 *
 */
public class InitialPlacementAction extends OptimisationAction {
	/**
	 * Virtual machine that is placed
	 */
	VirtualMachine vm;
	/**
	 * Physical machine to place virtual machine
	 */
	PhysicalMachine target;
	
	/**
	 * 
	 * @param vm Virtual machine that is placed
	 * @param target Physical machine to place virtual machine
	 */
	public InitialPlacementAction(VirtualMachine vm, PhysicalMachine target) {
		this.vm = vm;
		this.target = target;
	}
}
